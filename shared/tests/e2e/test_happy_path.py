def test_happy_path(order_request, jwt_token, frontend_api_url):
    """
    Test an order journey with a happy path
    """

    # Create the order
    query = """
    mutation ($order: CreateOrderRequest!) {
        createOrder(order: $order) {
            success
            message
            errors
            order {
                orderId
            }
        }
    }
    """
    variables = {
        "order": order_request
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("CREATE ORDER", body)
    assert body["data"]["createOrder"]["success"] == True

    order_id = body["data"]["createOrder"]["order"]["orderId"]

    # Wait
    time.sleep(5)

    # Retrieve the packaging request
    query = """
    query ($input: PackagingInput!) {
        getPackagingRequest(input: $input) {
            orderId
            status
            products {
                productId
                quantity
            }
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("GET PACKAGE REQUEST", body)
    pr = body["data"]["getPackagingRequest"]
    packaging_products = {p["productId"]: p for p in pr["products"]}
    for product in order_request["products"]:
        assert product["productId"] in packaging_products.keys()
        assert packaging_products[product["productId"]]["quantity"] == product.get("quantity", 1)

    # Start working on the packaging request
    query = """
    mutation ($input: PackagingInput!) {
        startPackaging(input: $input) {
            success
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("START PACKAGING", body)
    assert body["data"]["startPackaging"]["success"] == True

    # Complete the packaging request
    query = """
    mutation ($input: PackagingInput!) {
        completePackaging(input: $input) {
            success
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("COMPLETE PACKAGING", body)
    assert body["data"]["completePackaging"]["success"] == True

    # Wait
    time.sleep(5)

    # Check the order
    query = """
    query($orderId: ID!) {
        getOrder(orderId: $orderId) {
            orderId
            status
        }
    }
    """
    variables = {
        "orderId": order_id
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("CHECK ORDER", body)
    assert body["data"]["getOrder"]["status"] == "PACKAGED"

    # Retrieve the delivery request
    query = """
    query($input: DeliveryInput!) {
        getDelivery(input: $input) {
            orderId
            address {
                name
                companyName
                streetAddress
                postCode
                city
                state
                country
                phoneNumber
            }
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("GET DELIVERY", body)
    assert body["data"]["getDelivery"]["orderId"] == order_id
    assert body["data"]["getDelivery"]["address"] == order_request["address"]

    # Start delivery
    query = """
    mutation ($input: DeliveryInput!) {
        startDelivery(input: $input) {
            success
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("START DELIVERY", body)
    assert body["data"]["startDelivery"]["success"] == True

    # Complete delivery
    query = """
    mutation ($input: DeliveryInput!) {
        completeDelivery(input: $input) {
            success
        }
    }
    """
    variables = {
        "input": {
            "orderId": order_id
        }
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("COMPLETE DELIVERY", body)
    assert body["data"]["completeDelivery"]["success"] == True

    # Wait
    time.sleep(5)

    # Check the order
    query = """
    query($orderId: ID!) {
        getOrder(orderId: $orderId) {
            orderId
            status
        }
    }
    """
    variables = {
        "orderId": order_id
    }
    res = requests.post(
        frontend_api_url,
        headers={"Authorization": jwt_token},
        json={
            "query": query,
            "variables": variables
        }
    )
    body = res.json()
    print("CHECK ORDER", body)
    assert body["data"]["getOrder"]["status"] == "FULFILLED"